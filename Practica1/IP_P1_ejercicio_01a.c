#include <stdio.h>
#include <stdlib.h>
int main()
{
    printf("El número es %d \n", 15); //15 por ser %d tipo decimal
    printf("El número es %i \n", 15);//15 por se %i tb decimal
    printf("El número es %x \n", 15);//f hexadecimal = 15 base10
    printf("El número es %o \n", 15);//17 en base octal = 15 base10
    printf("El número es %e \n", 15.0);//saca el 15 en notacion cientifica
    printf("El número es %E \n", 15.0);//saca el 15 en notacion cientifica
    return 0;
}

